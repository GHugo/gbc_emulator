memory* memory_init(GB* rom) {
	memory* mem = malloc(sizeof(memory));
	if (mem == NULL)
		ERROR("Unable to allocate memory for memory structure.\n");

	mem->in_bios = 1;
	mem->bios = standard_bios;

	mem->rom = gbc_load_in_memory(rom);
	if (mem->rom == NULL)
		ERROR("Unable to load ROM into memory.\n");

	mem->gpu = calloc(0x2000, sizeof(uint8_t));
	if (mem->gpu == NULL)
		ERROR("Unable to allocate memory for gpu.\n");

	mem->external = calloc(0x2000, sizeof(uint8_t));
	if (mem->external == NULL)
		ERROR("Unable to allocate memory for external RAM.\n");

	mem->working = calloc(0x2000, sizeof(uint8_t));
	if (mem->working == NULL)
		ERROR("Unable to allocate memory for working RAM.\n");

	mem->sprites = malloc(sizeof(uint8_t) * 0xA0);
	if (mem->sprites == NULL)
		ERROR("Unable to allocate memory for graphics sprites.\n");

	mem->zero = calloc(0x80, sizeof(uint8_t));
	if (mem->zero == NULL)
		ERROR("Unable to allocate memory for Zero page.\n");

	return mem;
}

void memory_end(memory *mem) {
	free(mem->gpu);
	free(mem->external);
	free(mem->working);
	free(mem->sprites);
	free(mem->zero);
	free(mem);
}

void memory_set_bios(memory* mem, uint8_t status) {
	mem->in_bios = status;
}

static uint8_t* memory_get_offset(memory* mem, uint16_t addr) {
	switch ((addr & 0xF000) >> 12) {
		// Cartridge ROM, bank 0
	case 0x0:
		// BIOS
		if ((addr & 0xFF00) == 0 && mem->in_bios)
			return mem->bios;
		else
			return mem->rom;

	case 0x1:
	case 0x2:
	case 0x3:
		return mem->rom;

		// Cartridge ROM, other banks
	case 0x4:
	case 0x5:
	case 0x6:
	case 0x7:
		return mem->rom;

		// Graphics RAM
	case 0x8:
	case 0x9:
		return mem->gpu - 0x8000;

		// Cartridge (External) RAM
	case 0xA:
	case 0xB:
		return mem->external - 0xA000;

		// Working RAM
	case 0xC:
	case 0xD:
		return mem->working - 0xC000;

		// Working RAM (shadow)
	case 0xE:
		printf("In working\n");
		return mem->working - 0xE000;

	case 0xF:
		// Working RAM (shadow)
		if (((addr & 0x0F00) >> 8) < 0xE)
			return mem->working - 0xE000;

		// Graphics: sprite information
		if (((addr & 0x0F00) >> 8) == 0xE) {
			if (((addr & 0x00F0) >> 4) < 0xA)
				return mem->sprites - 0xFE00;
			else
				return NULL;
		}

		// Zero page
		if (addr >= 0xFF80)
			return mem->zero - 0xFF80;

		// I/O control
		ERROR("I/O still not handled.\n");
	}

	ERROR("Unknown addr 0x%X\n", addr);
}

uint8_t memory_read_byte(memory* mem, uint16_t addr) {
	uint8_t* offset = memory_get_offset(mem, addr);
	if (offset == NULL)
		return 0;

	return *(uint8_t*)(offset + addr);
}

uint16_t memory_read_word(memory* mem, uint16_t addr) {
	uint8_t* offset = memory_get_offset(mem, addr);
	if (offset == NULL)
		return 0;

	return *(uint16_t*)(offset + addr);
}

void memory_write_byte(memory* mem, uint16_t addr, uint8_t value) {
	uint8_t* offset = memory_get_offset(mem, addr);
	if (offset == NULL)
		ERROR("WRITING in OAM!\n");

	*(uint8_t*)(offset + addr) = value;
}

void memory_write_word(memory* mem, uint16_t addr, uint16_t value) {
	uint8_t* offset = memory_get_offset(mem, addr);
	if (offset == NULL)
		ERROR("WRITING in NULL zone!\n");

	*(uint16_t*)(offset + addr) = value;
}
